package com.napier.sem;

/**
 * Represents a city
 */
public class City {

    /**
     * the city ID
     */
    public int ID;

    /**
     * the Cities name
     */
    public String name;

    /**
     * the country code where the city resides
     */
    public String countryCode;

    /**
     * the District the city is located
     */
    public String district;

    /**
     * the cities population
     */
    public int population;
}
